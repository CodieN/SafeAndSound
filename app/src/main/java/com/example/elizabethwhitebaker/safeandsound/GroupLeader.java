package com.example.elizabethwhitebaker.safeandsound;

public class GroupLeader {
    private int groupLeaderID;
    private int initiatorID;
    private int groupID;

    public GroupLeader() {}

    public int getGroupLeaderID() { return groupLeaderID; }
    public void setGroupLeaderID(int groupLeaderID) { this.groupLeaderID = groupLeaderID; }
    public int getInitiatorID() {
        return initiatorID;
    }
    public int getGroupID() {
        return groupID;
    }
}
